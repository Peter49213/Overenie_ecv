package com.example.overenie_ecv;

public class Employees {
    String name, plate, surname;
    String key;

    public void setKey(String key) {
        this.key = key;
    }

    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }

    public String getPlate() {
        return plate;
    }

    public String getSurname() {
        return surname;
    }
}
